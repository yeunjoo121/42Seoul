#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	index;

	index = 0;
	while (index < n)
	{
		if (((unsigned char *)s1)[index] != ((unsigned char *)s2)[index])
		{
			return ((int)((unsigned char *)s1)[index]
					- ((unsigned char *)s2)[index]);
		}
		else
			index++;
	}
	return (0);
}
